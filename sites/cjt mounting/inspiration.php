	   <h3>Our Inspiration</h3>
	   <p>Throughout our lives, we meet people that inspire us and in doing so change the course of our lives.  In 1991, Tom and Carrie, the father and daughter team of CJT met with two such people;  A young man of sixteen who had just received his first communication device and his therapist, a woman committed to ensuring her students achieved their full potential.</p>
<p>The meeting occurred shortly after the LA Times published an article about the "Screen Beanie" an anti-glare visor for computer monitors.  A local therapist contacted CJT after reading the article.  She wanted to know if the Screen Beanie would fit on her student's Augmentative and Alternative communication device.  Not knowing what an AAC device was Carrie scheduled a meeting, unaware of the life changing impact it would have. </p>
<p>Tom and Carrie arrived at the meeting and learned from the therapist that the young man was refusing to go outside because he was unable to use his communication device.  She was hoping that the Screen Beanie would be able to shade the face of the device and allow her student to see and use his device outside.  Still, not completely understanding what an AAC device was Tom and Carrie met the young man.  When asked to go outside to try the Screen Beanie the student was not at all pleased and let everyone know.  However, with a bit of coaxing and some reassurance he agreed.  Once outside Tom placed the sample visor he had brought on the device.  To his and Carrie's surprise, the sample visor fit perfectly and did exactly what the therapist had hoped.   They turned away from the student to answer a question and from behind them they heard "Thank you very much I needed that".  When they turned back around and saw the young man's face, his pride and confidence showing, they understood what  a communication device could do and how much it meant to the young man to be able to personally express himself whenever and wherever. </p>
<p>It was a life changing moment for both Tom and Carrie, one that would shape the future of their company, the products they designed and their commitment to help.
<p>Tom and Carrie continued to learn about AAC devices and the field of Assistive Technology from the therapist.  They learned of the therapist's commitment to help her students achieve their full potential using AAC devices and about the challenges, her students faced in finding the right products.  Tom and Carrie realized they wanted to help, but were unsure of how. </p>
<p>Then one day during a visit to the school the answer came.   A representative from an AAC company was at the school training the staff.  The therapist introduced Tom and Carrie to the rep as problem solvers.    A few weeks later Tom and Carrie ran into the rep at a local show and she asked if CJT had anything or could design a product that an ambulatory person could use to transport his/her device.  Subsequently, in 1992 the "Walk it" a four-wheel mobile unit was released and CJT became a problem solving company, committed to developing quality mounting and positioning products, which promote the growth, productivity and potential of individuals. </p>
<p>Nearly twenty years have passed and CJT continues to develop its unique line of mounting, mobility and positioning products.  Tom and Carrie will never forget that first student or his therapist as they continue to strive to produce products that allow a person to communicate through the use of technology anytime or anywhere. </p>
